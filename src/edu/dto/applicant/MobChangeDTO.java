package edu.dto.applicant;

public class MobChangeDTO {
	private String ApplicationID;
	private String Mobile;
	private String TTNumber;
	private String SCode;
	private String AppWeb;
	private String AppSms;
	private String BoardName;
	
	
	public String getApplicationID() {
		return ApplicationID;
	}
	public void setApplicationID(String applicationID) {
		ApplicationID = applicationID;
	}
	public String getMobile() {
		return Mobile;
	}
	public void setMobile(String mobile) {
		Mobile = mobile;
	}
	public String getTTNumber() {
		return TTNumber;
	}
	public void setTTNumber(String tTNumber) {
		TTNumber = tTNumber;
	}
	public String getSCode() {
		return SCode;
	}
	public void setSCode(String sCode) {
		SCode = sCode;
	}
	public String getAppWeb() {
		return AppWeb;
	}
	public void setAppWeb(String appWeb) {
		AppWeb = appWeb;
	}
	public String getAppSms() {
		return AppSms;
	}
	public void setAppSms(String appSms) {
		AppSms = appSms;
	}
	public String getBoardName() {
		return BoardName;
	}
	public void setBoardName(String boardName) {
		BoardName = boardName;
	}
	
	

}
